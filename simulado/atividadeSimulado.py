numero = int(input("Qual número deseja?: "))

if numero % 2 == 0:
    print("Seu número é par!")
    multiPar = numero * 2
    print("O resultado multiplicado por 2 é:", multiPar)
else:
    print("Seu número é ímpar!")
    multiImpar = numero * 3
    print("O resultado multiplicado por 3 é:", multiImpar)

print()  # Linha em branco para separar

if numero < 10:
    conta = numero + 10 * 5 
    print("O resultado da conta menor que 10 é:", conta)





    

    

    

